static float fGetOffset(float fValue1, float fValue2, float fValueDesired)
{
        double fDelta = fValue2 - fValue1;

        if(fDelta == 0.0)
        {
                return 0.5;
        }
        return (fValueDesired - fValue1)/fDelta;
}

static int vMarchCube(float* data, int w, int h, int d, std::vector<btVector3>& verts, std::vector<btVector3>& normals, int fX, int fY, int fZ, int fScale, float targetValue)
{
	/*
        extern int aiCubeEdgeFlags[256];
        extern int a2iTriangleConnectionTable[256][16];
				*/
        int iCorner, iVertex, iVertexTest, iEdge, iTriangle, iFlagIndex, iEdgeFlags;
        float fOffset;
        float afCubeValue[8];
        float asEdgeVertex[12][3];
        //float asEdgeNorm[12][3];

        //Make a local copy of the values at the cube's corners
        for(iVertex = 0; iVertex < 8; iVertex++)
        {
                int x = fX + a2fVertexOffset[iVertex][0] * fScale;
                int y = fY + a2fVertexOffset[iVertex][1] * fScale;
                int z = fZ + a2fVertexOffset[iVertex][2] * fScale;
                int idx = z*w*h + y*w + x;
                assert(idx >= 0 && idx < w*h*d);
                afCubeValue[iVertex] = data[idx];
                assert(std::isfinite(afCubeValue[iVertex]));
        }

        //Find which vertices are inside of the surface and which are outside
        iFlagIndex = 0;
        for(iVertexTest = 0; iVertexTest < 8; iVertexTest++)
        {
                if(afCubeValue[iVertexTest] <= targetValue)
                        iFlagIndex |= 1<<iVertexTest;
        }

        //Find which edges are intersected by the surface
        iEdgeFlags = aiCubeEdgeFlags[iFlagIndex];

        //If the cube is entirely inside or outside of the surface, then there will be no intersections
        if(iEdgeFlags == 0)
        {
                return 0;
        }

        //Find the point of intersection of the surface with each edge
        //Then find the normal to the surface at those points
        for(iEdge = 0; iEdge < 12; iEdge++)
        {
                //if there is an intersection on this edge
                if(iEdgeFlags & (1<<iEdge))
                {
                        fOffset = fGetOffset(afCubeValue[ a2iEdgeConnection[iEdge][0] ],
                                                     afCubeValue[ a2iEdgeConnection[iEdge][1] ], targetValue);

                        asEdgeVertex[iEdge][0] = fX + (a2fVertexOffset[ a2iEdgeConnection[iEdge][0] ][0]  +  fOffset * a2fEdgeDirection[iEdge][0]) * fScale;
                        asEdgeVertex[iEdge][1] = fY + (a2fVertexOffset[ a2iEdgeConnection[iEdge][0] ][1]  +  fOffset * a2fEdgeDirection[iEdge][1]) * fScale;
                        asEdgeVertex[iEdge][2] = fZ + (a2fVertexOffset[ a2iEdgeConnection[iEdge][0] ][2]  +  fOffset * a2fEdgeDirection[iEdge][2]) * fScale;

/*
                        asEdgeNorm[iEdge];

                        float x = asEdgeVertex[iEdge].fX;
                        float y = asEdgeVertex[iEdge].fY;
                        float z = asEdgeVertex[iEdge].fZ;
                        float x_ = data[z*w*h + y*w + x+1];
                        float y_ = data[z*w*h + (y+1)*w + x];
                        float z_ = data[(z+1)*w*h + y*w + x];
                        */
                }
        }


        //Draw the triangles that were found.  There can be up to five per cube
        for(iTriangle = 0; iTriangle < 5; iTriangle++)
        {
                if(a2iTriangleConnectionTable[iFlagIndex][3*iTriangle] < 0)
                        return iTriangle;

                for(iCorner = 0; iCorner < 3; iCorner++)
                {
                        iVertex = a2iTriangleConnectionTable[iFlagIndex][3*iTriangle+iCorner];
                        btVector3 v(asEdgeVertex[iVertex][0], asEdgeVertex[iVertex][1], asEdgeVertex[iVertex][2]);                        
                        verts.push_back(v);
                }
        }
        return 5;
}

bool MarchingCubes::triangulateGrid(const uint8_t* data,
                        int width, int height,
                        std::vector<btVector3>& verts, std::vector<btVector3>& normals)
{

  float threshold = 1.5f;
  int step = 1.0f;

  const int depth = 50;
  int sz = width*height*depth;
  float * voxels = new float[sz];
  memset(voxels, 0, sz);

  Log::info("Generating voxel data");
  for (int y=0; y < height; ++y) {
    for (int x=0; x < width; ++x) {
      for (int z=10; z <= 10; ++z) {
        float magnitude = 0.01f + data[y * width + x] / 255.0f;
        magnitude = 10*(1 - magnitude);
        int xstart = x-magnitude < 0 ? x : magnitude;
        int xend = x+magnitude >= width ? width-1-magnitude : magnitude;
        for (int dx=-xstart; dx <= xend; ++dx) {
          int xi = x+dx;

          int ystart = y-magnitude < 0 ? y : magnitude;
          int yend = y+magnitude >= height ? height-1-magnitude : magnitude;
          for (int dy=-ystart; dy <= yend; ++dy) {
            int yi = y+dy;

            for (int dz=-magnitude; dz <= 4*magnitude; ++dz) {
              int zi = z+dz;
              if (zi < 0 || zi >= depth)
                continue;

              float fakedz = dz < 0 ? dz : dz/4.0f;

              float length2 = btVector3(dx, dy, fakedz).length2();
              assert(std::isfinite(length2));
              //							length2 = fabs(dx) + fabs(dy) + fabs(fakedz);
              float m = std::max(1 - length2/magnitude, 0.0f);
              m *= m*m;
              assert(std::isfinite(m));

              voxels[zi*width*height + yi*width + xi] += m;
            }
          }
        }
      }
    }
  }

  float maxim = *std::max_element(voxels, voxels + width*height*depth);
  Log::info("max: %f", maxim);
  uint8_t * tmp = new uint8_t[width*height*depth];
  for (int i=0; i < width*height*depth; ++i) {
    tmp[i] = (255 * voxels[i])/maxim;
  }

  FILE* fp = fopen("terrain.raw", "w+");
  fwrite(tmp, width*height*depth, 1, fp);
  fclose(fp);
  delete[] tmp;

  for (int y=1; y < height-1; y++) {
    for (int x=1; x < width-1; x++ ) {
      for (int z=1; z < depth-1; ++z) {
        vMarchCube(voxels, width, height, depth, verts, normals, x, y, z, 1, threshold);
      }
    }
    if (y % 100 == 0) Log::info("Marching slice %d", y);
  }

  /// the normal calculation doesn't represent this version anymore, so just calculate plane normals
  normals.reserve(verts.size());
  Log::info("Calculating normals");
#if 0
  for (unsigned int i=0; i < verts.size(); i+=3) {
    btVector3 & v1 = verts[i];
    btVector3 & v2 = verts[i+1];
    btVector3 & v3 = verts[i+2];
    btVector3 n = (v2-v1).cross(v3-v1).normalize();
    normals.insert(normals.end(), 3, n);
  }
#else
  const int max_rad = 12;
  // try to calculate normals from scalar field gradient
  for (int i=0; i<verts.size(); ++i) {		
    btVector3 & v = verts[i];
    btVector3 grad(0, 0, 0);
    int x = v.x()+0.5; int y = v.y()+0.5; int z = v.z()+0.5;
    if (i % 100000 == 0)
      Log::info("%f %f %f", v.x(), v.y(), v.z());
    float dz = v.z() - 10.0;
    float dz_ = dz > 0 ? dz / 4.0f : dz;
    //if (dz > 0) dz /= 4;
    for (int dx=-max_rad; dx <= max_rad; ++dx) {
      int xi = x+dx;
      if (xi < 0 || xi >= width) continue;
      for (int dy=-max_rad; dy <= max_rad; ++dy) {
        int yi = y+dy; 
        if (yi < 0 || yi >= height) continue;
        float magnitude = 0.01f + data[yi*width + xi] / 255.0f;
        magnitude = 10*(1 - magnitude);
        float dx_ = dx+(v.x() - x);
        float dy_ = dy+(v.y() - y);
        float length2 = btVector3(dx_, dy_, dz_).length2();
        //if (length2 > magnitude) continue;
        float up;
        if (dz >= 0) {
          up = length2/magnitude;
          up /= magnitude;
          up -= 1;
          up *= up;
          up *= -6;
          btVector3 n = up * btVector3(dx_, dy_, dz/16.0);
          grad += n;
        } else {
          up = length2 - magnitude;
          up *= up;
          float r6 = magnitude;
          r6 *= r6*r6;
          up /= r6;
          up *= -6;
          btVector3 n = up * btVector3(dx_, dy_, dz);
          grad += n;
        }
      }
    }
    // if error 
    //assert(!grad.isZero());
    grad.normalize();
    normals.push_back(grad);
  }
#endif

  delete[] voxels;
  Log::info("Marching done");
  return true;
}
